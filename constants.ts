// Helper function for Income Selector
export const getIncomeRanges = (country: Country, language: string = 'en'): IncomeRange[] => {
  const t = TRANSLATIONS[language] || TRANSLATIONS['en'];
  const scale = country.baseScale;
  const currency = country.currencySymbol;
  const fmt = (n: number) => n.toLocaleString();

  return [
    { min: 0, max: 0, label: t.unemployed }, // 0 / Unemployed
    { min: 1, max: scale, label: `${currency}1 - ${currency}${fmt(scale)}` }, // 1 to Scale (Starting)
    { min: scale, max: scale * 2, label: `${currency}${fmt(scale)} - ${currency}${fmt(scale * 2)}` },
    { min: scale * 2, max: scale * 4, label: `${currency}${fmt(scale * 2)} - ${currency}${fmt(scale * 4)}` },
    { min: scale * 4, max: scale * 8, label: `${currency}${fmt(scale * 4)} - ${currency}${fmt(scale * 8)}` },
    { min: scale * 8, max: scale * 15, label: `${currency}${fmt(scale * 8)} - ${currency}${fmt(scale * 15)}` },
    { min: scale * 15, max: null, label: `${currency}${fmt(scale * 15)}+` },
  ];
};
